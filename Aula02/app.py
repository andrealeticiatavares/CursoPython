# Váriaveis
nome = "Manu"
idade = "13"
gosto_de = "jogar video game"

# Concatenar: juntar strings
print("Meu nome é " + nome + ".")

# f-string: forma eficiente de formatar strings em Python, adicionando váriaveis dentro da string.
print(f"Meu nome é {nome}.")
print(f"Tenho {idade} anos.")
print(f"Gosto de {gosto_de}.")

# Tipos de váriaveis
nome = "Roger" # Texto (String)
idade = 21     # Inteiro (int)
altura = 1.78  # Ponto flutuante (float)
homem = True   # Boolean (verdadeiro / falso)

print(f"Meu nome é {nome}.")
print(f"Tenho {idade} anos.")
print(f"Minha altura é {altura} metros.")
print(f"É {homem} que sou homem.")